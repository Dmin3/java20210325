package MyPratice;

public class Constru {
	int left, right;
	
	public Constru(int left, int right) {
		this.left = left;
		this.left = right;		
	}
	
	public void sum() {
		System.out.println(this.left + this.right);
	}
	
	public void avg() {
		System.out.println((this.left + this.right) / 2);
	}
}
